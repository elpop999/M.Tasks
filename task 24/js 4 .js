function checkScore (arr){
    let res = {
        "#" : 5 , 
        "0" : 3 ,
        "X" : 1 ,
        "!" : -1,
        "!!" : -3 ,
        "!!!" : -5 ,
    }
    let score = 0
    for (let i = 0 ; i < arr.length ; i ++){
        for (let j = 0 ; j < arr[i].length ; j ++){
            score += res[arr[i][j]]
        }
    }
    return score > 0 ? score : 0
}

